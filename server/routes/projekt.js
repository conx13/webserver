// ────────────────────────────────────────────────────────────────────────────────
//
// ────────────────────────────────────────────────────────────────────── I ──────────
//   :::::: P R O J E K T I D E   L I S T : :  :   :    :     :        :          :
// ────────────────────────────────────────────────────────────────────────────────
//
router.get('/list', (req, res, next) => listProjekt(req, res, next));
// ────────────────────────────────────────────────────────────────────────────────
//
// ────────────────────────────────────────────────────────────────────── I ──────────
//   :::::: O T S I M E   P R O J E K T I : :  :   :    :     :        :          :
// ────────────────────────────────────────────────────────────────────────────────
//
router.get('/otsi/:otsitext', (req, res, next) => findProjekt(req, res, next));
// ────────────────────────────────────────────────────────────────────────────────

/**
*Export Module
*/
module.exports = router;
