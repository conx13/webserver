/**
*Create router instance
*/
const router = require('express').Router();

/**
*Module dependencies
*/
const projektCont = require('../controllers/projektCont');
/**
*Module Variables
*/
const {
  newProjekt,
  delProjekt,
  editProjekt,
  listProjekt,
  findProjekt,
} = projektCont;

//
// ────────────────────────────────────────────────────────────── I ──────────
//   :::::: U U S   P R O J E K T : :  :   :    :     :        :          :
// ────────────────────────────────────────────────────────────────────────
//
router.post('/uus', (req, res, next) => newProjekt(req, res, next));
// ────────────────────────────────────────────────────────────────────────────────
//
// ─────────────────────────────────────────────────────────────────────────────────── I ──────────
//   :::::: K U S T U T A M E   P R O J E K T I   J A   K A U S T A : :  :   :    :     :        :
// ────────────────────────────────────────────────────────────────────────────────────────────────
//
router.delete('/delete/:id', (req, res, next) => delProjekt(req, res, next));
// ────────────────────────────────────────────────────────────────────────────────
//
// ─────────────────────────────────────────────────────────────────────────────────── I ──────────
//   :::::: M U U D A M E   P R O J E K T I   A N D M E I D : :  :   :    :     :        :        :
// ────────────────────────────────────────────────────────────────────────────────────────────────
//
